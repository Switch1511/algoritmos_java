package fundamentacaoteorica;

public class EstruturaRepeticaoDoWhile {

	public static void main(Strings[] args) {
		int numero = 4;
		System.out.println("Inicio Programa");
		do {
			System.out.println(numero);
			numero = numero + 1;
		} while (numero < 4);
		System.out.println("Fim Programa");
	}

}
